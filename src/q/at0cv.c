static int atcv(atomo * ao, int q){
  int N;
  if(q > NELEMENTS){
    N = -1;
    return N;
  }
  else if(q<1){
    N = 0;
    return N;
  }
  else{
    N = end[q]-end[q-1];
  }
  ao->ng = N;
  ao->a  = a + end[q-1];
  ao->w  = c + end[q-1];
  return N;
}

void at0cv_prep(){
  // convert coefficients of gaussians with charge=1
  // to coefficients of non-normalized gaussians
  static int done = 0;
  if(done){
    return;
  }
  for(int i=0; i<sizeof(a)/sizeof(a[0]); i++){
    c[i] *= S1r2i(a[i]);
  }
  done = 1;
  return;
}

void at0cv_fill(atomo * ao, mol * m){
  for(int k=0; k<m->n; k++){
    ao[k].l = 0;
    ao[k].m = 0;
    ao[k].k = k;
    atcv(ao+k, m->q[k]);
  }
  return;
}

