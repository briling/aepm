static int atcv(atomo * ao, int q){
  int N;
  if(q > NELEMENTS){
    N = -1;
    return N;
  }
  else if(q<1){
    N = 0;
    return N;
  }
  else{
    N = end[q]-end[q-1];
  }
  ao->ng = N;
  ao->a  = a + end[q-1];
  ao->w  = c + end[q-1];
  return N;
}

void atcv_prep(){
  // convert coefficients of gaussians with charge=1
  // to coefficients of non-normalized gaussians
  static int done = 0;
  if(done){
    return;
  }
  for(int i=0; i<sizeof(a)/sizeof(a[0]); i++){
    c[i] *= S1r2i(a[i]);
  }
  done = 1;
  return;
}

void atcv_fill(atomo * ao, mol * m){
  for(int k=0; k<m->n; k++){
    ao[k].l = 0;
    ao[k].m = 0;
    ao[k].k = k;
    atcv(ao+k, m->q[k]);
  }
  return;
}

void atcv_nozzle_fill_all(atomo * ao, int * atpar, double * a, double * w, mol * m){
  for(int k=0; k<m->n; k++){
    int q = m->q[k];
    if(atpar[q]>=0){
      ao[k].l  = 0;
      ao[k].m  = 0;
      ao[k].ng = 1;
      ao[k].k  = k;
      ao[k].a  = a+atpar[q];
      ao[k].w  = w+atpar[q];
    }
    else{
      ao[k].l  = 0;
      ao[k].m  = 0;
      ao[k].ng = 0;
      ao[k].k  = 0;
      ao[k].a  = NULL;
      ao[k].w  = NULL;
    }
  }
  return;
}

void atcv_nozzle_fill_all1(atomo * ao, double * a, double * w, mol * m){
  for(int k=0; k<m->n; k++){
    int q = m->q[k];
    ao[k].l  = 0;
    ao[k].m  = 0;
    ao[k].ng = 1;
    ao[k].k  = k;
    ao[k].a  = a + q;
    ao[k].w  = w + q;
  }
  return;
}

